    @Override
    public final ChannelPipeline addLast(EventExecutorGroup executor, ChannelHandler... handlers) {
        ObjectUtil.checkNotNull(handlers, "handlers");

        for (ChannelHandler h: handlers) {

            // 这可能是netty的一种 handler 规范
            //很多地方都这样写,或许为了更快的结束循环 ..
            // 但如果 handler列表中包含了null,可能会有一定的bug(也就是开发人员会懵比,为什么自己的handler 没有执行) ...
            if (h == null) {
                break;
            }
            addLast(executor, null, h);
        }

        return this;
    }

    // 为ChannelHandler 生成一个 名字 ...
    private String generateName(ChannelHandler handler) {
        Map<Class<?>, String> cache = nameCaches.get();
        Class<?> handlerType = handler.getClass();
        String name = cache.get(handlerType);
        if (name == null) {
            name = generateName0(handlerType);
            cache.put(handlerType, name);
        }

        // It's not very likely for a user to put more than one handler of the same type, but make sure to avoid
        // any name conflicts.  Note that we don't cache the names generated here.

        // 以下这段代码给我一点启示, 为什么用foreach 而不是 Set / Map ...

        // 又是遍历Linked list 判断名称 ..
        if (context0(name) != null) {
            String baseName = name.substring(0, name.length() - 1); // Strip the trailing '0'.
            for (int i = 1;; i ++) {
                String newName = baseName + i;
                // 这里又来一次,难道这有什么性能提升?
                // 我认为这里通过一个Set 可能更好(但是,看了一下set实现(底层如果用map, 还要进行hash运算,比较麻烦)) ..
                // 当然如果数量很少的情况下,遍历linked list 一样的 (所以如果数量少的情况下,直接遍历效果最好) ...
                if (context0(newName) == null) {
                    name = newName;
                    break;
                }
            }
        }
        return name;
    }

    private static String generateName0(Class<?> handlerType) {
        return StringUtil.simpleClassName(handlerType) + "#0";
    }

